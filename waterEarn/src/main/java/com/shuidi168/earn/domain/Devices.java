package com.shuidi168.earn.domain;

/**
 * Devices entity. @author MyEclipse Persistence Tools
 */

public class Devices implements java.io.Serializable {

	// Fields

	private Integer id;
	private String osVersion;
	private String brand;
	private String model;

	// Constructors

	/** default constructor */
	public Devices() {
	}

	/** minimal constructor */
	public Devices(String osVersion, String brand) {
		this.osVersion = osVersion;
		this.brand = brand;
	}

	/** full constructor */
	public Devices(String osVersion, String brand, String model) {
		this.osVersion = osVersion;
		this.brand = brand;
		this.model = model;
	}

	// Property accessors

	public Integer getId() {
		return this.id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getOsVersion() {
		return this.osVersion;
	}

	public void setOsVersion(String osVersion) {
		this.osVersion = osVersion;
	}

	public String getBrand() {
		return this.brand;
	}

	public void setBrand(String brand) {
		this.brand = brand;
	}

	public String getModel() {
		return this.model;
	}

	public void setModel(String model) {
		this.model = model;
	}

}